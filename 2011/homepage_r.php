
<p>Conferinţa Studenţilor, Profesorilor şi Cercetătorilor Români din Marea Britanie este o iniţiativă comuna a studenţilor români din Marea Britanie si Ambasadei Romaniei, ce are drept obiectiv crearea de legături între studenţii, cercetătorii şi profesorii români din Regatul Unit, precum şi dezbaterea unor teme de interes propuse de aceştia, inclusiv a oportunităţilor de carieră în România.</p>


<p>La 22 octombrie 2011, va avea loc cea de-a patra ediţie a acestei conferinţe. Evenimentul se va desfăşura sub patronajul Ambasadei României la Londra, Camerei de Comerţ Britanică Română şi Fundaţiei Raţiu, cu sprijinul companiei Smith&Felton şi în colaborare cu Institutul Cultural Român - Londra.</p>

<p>Primele trei ediţii ale Conferinţei au avut loc la 24 octombrie 2008, 24 octombrie 2009 şi 23 octombrie 2010.</p>

<p>Din Comitetul de organizare fac parte studenţi, cercetători şi profesori de la universităţile Canterbury Christ Church, Edinburgh, Kent, Manchester, Oxford, precum şi reprezentanti ai Ambasadei României la Londra şi Camerei de Comerţ Britanică Română.</p>

<p>Comitetul de organizare a hotărât ca ediţia de anul acesta a Conferinţei să aibă ca temă principală “Cum să contruieşti o carieră de succes” şi să fie structurată în două părţi: o sesiune plenară şi o sesiune de paneluri de dezbatere cu următoarele teme: Cum să reuşeşti: o abordare practică; Cum să ai o carieră de succes – studii de caz; Cum să obţii avantaje din relaţionare şi parteneriat.</p>

<p>Conferinţa se va încheia, ca în fiecare an, cu decernarea “Diplomei Ambasadorului”.</p>

